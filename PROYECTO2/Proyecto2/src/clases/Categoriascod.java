package clases;

/**
 *
 * @author Pedro
 */
public class Categoriascod {
    
    private String usuarion;
    private String categoria;

    public Categoriascod() {
    }
    

    public Categoriascod(String usuarion, String categoria) {
        this.usuarion = usuarion;
        this.categoria = categoria;
    }

    public String getUsuarion() {
        return usuarion;
    }

    public String getCategoria() {
        return categoria;
    }


    public void setUsuarion(String usuarion) {
        this.usuarion = usuarion;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }
    

}
